namespace OpexSDK.Enumerations
{
    /// <summary>
    /// Describes the bit depth of an image.
    /// </summary>
    public enum ImageDepth
    {
        /// <summary>
        /// 1-bit (black and white)
        /// </summary>
        Bitonal = 1,

        /// <summary>
        /// 8-bit grayscale
        /// </summary>
        Grayscale = 8,

        /// <summary>
        /// 24-bit color
        /// </summary>
        Color = 24
    }
}